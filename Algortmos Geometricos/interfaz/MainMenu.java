    public void genera(int t){
        
        int r,s, maxr=1200, minr=0,maxs=600, mins=0;
        int i,j,h=0;
        
        i=0;
        while(i<t){
            r = (int)(Math.random()*(maxr-minr)+minr);
            s = (int)(Math.random()*(maxs-mins)+mins);
            
            if(lista.isEmpty()){
                Punto p = new Punto(r,s);
                lista.add(p);
            }
            else{
                j=0;
                while(j<lista.size()){
                    if(lista.get(j).getX()!=r && lista.get(j).getY()!=s){
                        h=1;
                    }
                    else{
                        h=0;
                        j=lista.size();
                    }
                    j++;
                }
                
                if(h==1){
                    Punto p = new Punto(r,s);
                    lista.add(p);
                }
            }
            i++;
        }
    }
